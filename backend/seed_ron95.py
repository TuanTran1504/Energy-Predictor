def seed():
    conn = psycopg2.connect(DATABASE_URL)
    cur = conn.cursor()
    inserted = skipped = 0

    for date_str, price in HISTORICAL_PRICES:
        cur.execute("""
            INSERT INTO ron95_prices
                (announced_at, effective_at, price_vnd, source_url, notes)
            VALUES (%s, %s, %s, %s, %s)
            ON CONFLICT (effective_at) DO NOTHING
        """, (
            f"{date_str} 00:00:00+00",
            f"{date_str} 00:00:00+00",
            price,
            "https://giaxanghomnay.com",
            "Manually curated from public records",
        ))
        if cur.rowcount > 0:
            inserted += 1
        else:
            skipped += 1

    conn.commit()
    cur.close()
    conn.close()
    print(f"Done. Inserted: {inserted} · Skipped: {skipped} duplicates")
    print(f"Price range: {min(p for _,p in HISTORICAL_PRICES):,} – {max(p for _,p in HISTORICAL_PRICES):,} VND")

